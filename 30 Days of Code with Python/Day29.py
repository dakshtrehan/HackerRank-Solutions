def bitwiseAnd(N, K):
    # Write your code here
    mx_b = 0
    for x in range(1,N+1):
        for y in range(1,x):
            b = x&y
            if mx_b<b<K:
                mx_b = b
                if mx_b == K-1:
                    return mx_b
    return mx_b        

